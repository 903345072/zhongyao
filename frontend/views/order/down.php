function use_discount() {
	discount = 0;
	discount = parseFloat($('#discount').val());
	jiaoyi_price = parseFloat($('#jiaoyifei').html());
	if(discount > jiaoyi_price) {
		$('#discount').val('0');
		layer.alert("积分只能抵扣交易费，您使用的积分已经超过了交易费，请重新选择！");
		return false;
	}
	set_totalprice();
}

function set_totalprice() {
	nums = parseInt($('#goodsnums').val());
	if($('input[name=buytype]:checked').val() == 2) {
		price = $('#limitprice').val();
	} else {
		price = $('#lastprice').val();
